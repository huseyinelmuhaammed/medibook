  static List<String> getDoluSlotlar(DateTime tarih) {
    final dayOfMonth = tarih.day;
    if (dayOfMonth % 3 == 0) {
      return ['09:30', '10:30', '14:00'];
    } else if (dayOfMonth % 3 == 1) {
      return ['10:00', '11:30'];
    } else {
      return ['09:00', '15:00', '16:00'];
    }
  }
